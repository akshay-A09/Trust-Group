document.addEventListener("DOMContentLoaded", function() {
  var tabs = document.querySelectorAll('.tab');
  tabs.forEach(function(tab) {
    tab.addEventListener('click', function() {
      var tabId = this.getAttribute('data-tab');
      activateTab(tabId);
      history.pushState(null, null, '#' + tabId);
    });
  });
  var hash = window.location.hash.substr(1);
  if (hash !== "") {
    activateTab(hash);
  }
  function activateTab(tabId) {
    var tabContents = document.querySelectorAll('.tab-content');
    tabContents.forEach(function(content) {
      content.classList.remove('active');
    });
    document.getElementById(tabId).classList.add('active');

    tabs.forEach(function(t) {
      t.classList.remove('active');
      if (t.getAttribute('data-tab') === tabId) {
        t.classList.add('active');
      }
    });
  }
});


// counting effect
function animateCount(element) {
        var endValue = parseInt(element.textContent);
        var duration = 2000;
        var interval = 10;
        var step = (endValue / duration) * interval;
        var current = 0;

        var timer = setInterval(function () {
            current += step;
            if (current >= endValue) {
                clearInterval(timer);
                element.textContent = endValue;
            } else {
                element.textContent = Math.round(current);
            }
        }, interval);
    }

    function startCounting(entries, observer) {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                observer.unobserve(entry.target);
                var counts = entry.target.getElementsByClassName('count');
                for (var i = 0; i < counts.length; i++) {
                    animateCount(counts[i]);
                }
            }
        });
    }

    var options = {
        root: null,
        rootMargin: '0px',
        threshold: 0.6
    };

    var observer = new IntersectionObserver(startCounting, options);
    var target = document.getElementById('aboutSection');
    observer.observe(target);
// counting effect End

