  document.addEventListener("DOMContentLoaded", function() {
    // Get all awards items
    var awardsItems = document.querySelectorAll('.awards_accolades_item');

    // Initially hide all items except the first 5
    for (var i = 5; i < awardsItems.length; i++) {
      awardsItems[i].style.display = 'none';
    }

    // Toggle visibility of items when clicking "View More" button
    document.getElementById('viewMoreBtn').addEventListener('click', function(e) {
      e.preventDefault();
      for (var i = 5; i < awardsItems.length; i++) {
        if (awardsItems[i].style.display === 'none') {
          awardsItems[i].style.display = '';
          this.querySelector('span').textContent = 'View Less';
        } else {
          awardsItems[i].style.display = 'none';
          this.querySelector('span').textContent = 'View More';
        }
      }
    });
  });
